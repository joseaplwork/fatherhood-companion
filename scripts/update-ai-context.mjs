import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

import { collectDeclaredDependencies, loadWorkspaces, toRepoPath } from "./lib/workspaces.mjs";

const repoRoot = process.cwd();
const outputPath = path.join(repoRoot, "docs/ai/context-index.generated.md");
const checkOnly = process.argv.includes("--check");

const rootPackageJson = JSON.parse(await readFile(path.join(repoRoot, "package.json"), "utf8"));
const workspaces = await loadWorkspaces(repoRoot);
const workspaceNames = new Set(workspaces.map((workspace) => workspace.name));
const generatedContent = renderContextIndex({
  rootPackageJson,
  workspaces,
  workspaceNames,
});

if (checkOnly) {
  const currentContent = await safeRead(outputPath);

  if (currentContent !== generatedContent) {
    console.error("docs/ai/context-index.generated.md is out of date. Run `pnpm context:update`.");
    process.exit(1);
  }

  console.log("AI context index is up to date.");
  process.exit(0);
}

await writeFile(outputPath, generatedContent);
console.log(`Updated ${toRepoPath(repoRoot, outputPath)}.`);

function renderContextIndex({ rootPackageJson, workspaces, workspaceNames }) {
  const lines = [
    "# AI Context Index",
    "",
    "Generated by `pnpm context:update`.",
    "",
    "## Root Scripts",
    "",
    ...Object.entries(rootPackageJson.scripts ?? [])
      .sort(([left], [right]) => left.localeCompare(right))
      .map(([name, command]) => `- \`${name}\`: \`${command}\``),
    "",
    "## Workspaces",
    "",
  ];

  for (const workspace of workspaces) {
    const internalDependencies = collectDeclaredDependencies(workspace.packageJson)
      .filter((dependency) => workspaceNames.has(dependency))
      .sort();
    const scripts = Object.entries(workspace.packageJson.scripts ?? {}).sort(([left], [right]) =>
      left.localeCompare(right),
    );

    lines.push(`### ${workspace.name}`);
    lines.push("");
    lines.push(`- Kind: ${workspace.kind}`);
    lines.push(`- Path: \`${workspace.relativeDir}\``);
    lines.push(
      `- Internal dependencies: ${
        internalDependencies.length > 0
          ? internalDependencies.map((dependency) => `\`${dependency}\``).join(", ")
          : "none"
      }`,
    );
    lines.push("- Scripts:");

    for (const [name, command] of scripts) {
      lines.push(`  - \`${name}\`: \`${command}\``);
    }

    lines.push("");
  }

  lines.push("## Hard Boundaries");
  lines.push("");
  lines.push("- Apps compose packages, but no workspace may import another app.");
  lines.push("- Packages may depend on packages, but packages may never import apps.");
  lines.push("- Cross-workspace imports must use package names, not filesystem paths.");
  lines.push("- Deep imports into another workspace are forbidden.");
  lines.push(
    "- Repeated failures must be written into `docs/ai/mistake-log.md` and, when possible, promoted into automation.",
  );
  lines.push("");

  return `${lines.join("\n")}\n`;
}

async function safeRead(filePath) {
  try {
    return await readFile(filePath, "utf8");
  } catch {
    return null;
  }
}
